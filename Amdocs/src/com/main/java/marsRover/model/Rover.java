package com.main.java.marsRover.model;

public class Rover {

    private Integer xAxis;
    private Integer yAxis;
    private Character facing;

    public Rover() { }

    public Integer getXAxis() { return xAxis; }
    public void setXAxis(Integer xAxis) { this.xAxis = xAxis; }

    public Integer getYAxis() { return yAxis; }
    public void setYAxis(Integer yAxis) { this.yAxis = yAxis; }

    public Character getFacing() { return facing; }
    public void setFacing(Character facing) { this.facing = facing; }
}
